    public class Calculadora
    {
        #region COMPORTAMENTO

        /// <summary>
        /// 
        /// </summary>
        /// <param name="x"></param>
        /// <param name="y"></param>
        /// <returns></returns>
        public static int Soma(int x, int y)
        {
            return x + y;
        }

        /// <summary>
        /// 
        /// </summary>
        /// <param name="x"></param>
        /// <param name="y"></param>
        /// <returns></returns>
        public static int Sub(int x, int y)
        {
            return x - y;
        }

        #endregion
    }

